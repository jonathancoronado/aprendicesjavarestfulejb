    @Id
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Column(name = "NAME")
    private String name;
    @OneToMany(mappedBy = "idGroup")
    private Collection<Team> teamCollection;

    public Group01() {
    }

    public Group01(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

